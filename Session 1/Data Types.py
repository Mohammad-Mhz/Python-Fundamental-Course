## Number Data types
# int
print(5)
print(125453453)
print(-125)

print(type(5))
print(type(125453453))
print(type(-125))

# float
print(5.10)
print(1.2)
print(-125.35)
print(35e3)  
print(-87.7e100)

print(type(5.10))
print(type(1.2))
print(type(-125.35))
print(type(35e3))  
print(type(-87.7e100))

# complex
print(5+3j)
print(5j)
print(-9j)

print(type(5+3j))
print(type(5j))
print(type(-9j))

## String
print("Hello")  
print('Hello')

# Quotes Inside Quotes
print("It's alright")  
print("He is called 'Johnny'")  
print('He is called "Johnny"')
print("We are the so-called \"Vikings\" from the north.")

# Multiline Strings
print("""Lorem ipsum dolor sit amet,  
consectetur adipiscing elit,  
sed do eiusmod tempor incididunt  
ut labore et dolore magna aliqua.""")

## Boolean types
# Booleans represent one of two values: True or False.
print(10 > 9)  
print(10 == 9)  
print(10 < 9)