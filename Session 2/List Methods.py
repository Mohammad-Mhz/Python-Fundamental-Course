# Append Items
fruit = ["apple", "banana", "cherry"]  
fruit.append("orange")  
print(fruit)

# Insert Items
fruit = ["apple", "banana", "cherry"]  
fruit.insert(2, "watermelon")  
print(fruit)

# Extend List
fruit = ["apple", "banana", "cherry"]  
tropical = ["mango", "pineapple", "papaya"]  
fruit.extend(tropical)  
print(fruit)

# Remove Specified Item
fruit = ["apple", "banana", "cherry"]  
fruit.remove("banana")  
print(fruit)

# Remove Specified Index
fruit = ["apple", "banana", "cherry"]  
fruit.pop(1)  
print(fruit)